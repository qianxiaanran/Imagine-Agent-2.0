def _pick_source_path(repo_root: Path) -> Path:
    candidates = [
        repo_root / "artifacts" / "2026届毕业设计说明5.docx",
        repo_root / "artifacts" / "2026届毕业设计说明4_补全第六章.docx",
        repo_root / "artifacts" / "2026届毕业设计说明4.docx",
    ]
    last_error: Exception | None = None
    for candidate in candidates:
        if not candidate.exists():
            continue
        try:
            import zipfile

            with zipfile.ZipFile(candidate, "r") as zin:
                zin.read("word/document.xml")
            return candidate
        except Exception as exc:
            last_error = exc
            continue
    if last_error is not None:
        raise last_error
    raise FileNotFoundError("No available source docx found.")


def main() -> None:
    module = _load_common_module()
    repo_root = Path(__file__).resolve().parents[1]
    source_path = _pick_source_path(repo_root)
    output_path = repo_root / "artifacts" / "2026届毕业设计说明5_补全第七章.docx"
    fallback_output_path = repo_root / "artifacts" / "2026届毕业设计说明5_补全第七章_副本.docx"

    content = make_chapter7_content()

    with module.zipfile.ZipFile(source_path, "r") as zin:
        root = module.ET.fromstring(zin.read("word/document.xml"))
        body = root.find(module.qn("body"))
        if body is None:
            raise RuntimeError("word/document.xml missing body")

        paras = module.get_body_paragraphs(body)
        chapter_title_para = module.find_paragraph_by_exact_text(paras, "绪论")
        section_title_para = module.find_paragraph_by_exact_text(paras, "选题背景")
        subsection_title_para = module.find_paragraph_by_exact_text(paras, "国内研究现状")
        body_template_para = paras[paras.index(section_title_para) + 1]
        toc_chapter_template_para = next(
            para for para in paras if module.paragraph_text(para).startswith("1 绪论")
        )
        toc_section_template_para = next(
            para for para in paras if module.paragraph_text(para).startswith("1.1 ")
        )
        toc_subsection_template_para = next(
            para for para in paras if module.paragraph_text(para).startswith("1.2.1 ")
        )

        chapter_title_run = chapter_title_para.find(module.qn("r"))
        section_title_run = section_title_para.find(module.qn("r"))
        subsection_title_run = subsection_title_para.find(module.qn("r"))
        body_template_run = body_template_para.find(module.qn("r"))
        toc_chapter_run = toc_chapter_template_para.find(module.qn("r"))
        toc_section_run = toc_section_template_para.find(module.qn("r"))
        toc_subsection_run = toc_subsection_template_para.find(module.qn("r"))

        toc_anchor = chapter_title_para
        chapter_index = paras.index(chapter_title_para)
        for idx in range(chapter_index - 1, -1, -1):
            if module.paragraph_text(paras[idx]).strip():
                break
            toc_anchor = paras[idx]

        for level, text in content["toc"]:
            if level == "chapter":
                module.insert_paragraphs_before(
                    body,
                    toc_anchor,
                    toc_chapter_template_para,
                    toc_chapter_run,
                    [text],
                )
            elif level == "section":
                module.insert_paragraphs_before(
                    body,
                    toc_anchor,
                    toc_section_template_para,
                    toc_section_run,
                    [text],
                )
            else:
                module.insert_paragraphs_before(
                    body,
                    toc_anchor,
                    toc_subsection_template_para,
                    toc_subsection_run,
                    [text],
                )

        body_insertions: list[module.ET.Element] = []
        body_insertions.append(module.make_page_break_paragraph(body_template_para, body_template_run))

        chapter_para = module.deepcopy(chapter_title_para)
        module.set_paragraph_text(chapter_para, str(content["chapter_title"]), chapter_title_run)
        body_insertions.append(chapter_para)

        for item in content["sections"]:
            if item["level"] == "section":
                heading_para = module.deepcopy(section_title_para)
                heading_run = section_title_run
            else:
                heading_para = module.deepcopy(subsection_title_para)
                heading_run = subsection_title_run
            module.set_paragraph_text(heading_para, str(item["title"]), heading_run)
            body_insertions.append(heading_para)
            for text in item["paragraphs"]:
                para = module.deepcopy(body_template_para)
                module.set_paragraph_text(para, text, body_template_run)
                body_insertions.append(para)

        module.insert_before_sectpr(body, body_insertions)

        output_path.parent.mkdir(parents=True, exist_ok=True)
        target_path = output_path
        try:
            with module.zipfile.ZipFile(target_path, "w") as zout:
                for item in zin.infolist():
                    if item.filename != "word/document.xml":
                        zout.writestr(item, zin.read(item.filename))
                zout.writestr(
                    "word/document.xml",
                    module.ET.tostring(root, encoding="utf-8", xml_declaration=True),
                )
        except PermissionError:
            target_path = fallback_output_path
            with module.zipfile.ZipFile(target_path, "w") as zout:
                for item in zin.infolist():
                    if item.filename != "word/document.xml":
                        zout.writestr(item, zin.read(item.filename))
                zout.writestr(
                    "word/document.xml",
                    module.ET.tostring(root, encoding="utf-8", xml_declaration=True),
                )

    print(target_path)
    print(source_path)


if __name__ == "__main__":
    main()
